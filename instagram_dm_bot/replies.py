def get_reply(text: str) -> Optional[str]:
    """
    По тексту сообщения возвращает готовый ответ или None.
    Проверка по вхождению ключевой фразы в нижнем регистре.
    """
    if not text or not text.strip():
        return None
    lower = text.strip().lower()
    for keyword, reply in REPLIES.items():
        if keyword in lower:
            return reply
    return None


def get_default_reply() -> str:
    """Ответ, когда ни одно ключевое слово не подошло."""
